int cntColumns = ReadInt($"Колличество колонок");
int cntRows = ReadInt($"Колличество строк");
int[,] table = Generate2DArray(cntColumns, cntRows);
Print2DArray(table);
System.Console.WriteLine();
Print2DArray(Min(table));